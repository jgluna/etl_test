CREATE TABLE Products (
    `Index` INT,
    `Name` VARCHAR(255),
    `Description` TEXT,
    `Brand` VARCHAR(255),
    `Category` VARCHAR(255),
    `Price` DECIMAL(10, 2),
    `Currency` VARCHAR(3),
    `Stock` INT,
    `EAN` VARCHAR(13),
    `Color` VARCHAR(50),
    `Size` VARCHAR(50),
    `Availability` VARCHAR(20),
    `Internal ID` INT
);