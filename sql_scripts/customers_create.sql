CREATE TABLE Customers (
    Index INT,
    Customer_Id VARCHAR(50),
    First_Name VARCHAR(50),
    Last_Name VARCHAR(50),
    Company VARCHAR(255),
    City VARCHAR(100),
    Country VARCHAR(100),
    Phone_1 VARCHAR(20),
    Phone_2 VARCHAR(20),
    Email VARCHAR(100),
    Subscription_Date DATE,
    Website VARCHAR(255)
);
